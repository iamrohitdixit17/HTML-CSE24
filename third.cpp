#include<iostream>
using namespace std;
int main()
{
    int num1,num2;
    cout<<"The value of num 1 is:\n"; /*'<<' is called Insertion Operator*/
    cin>>num1; /*'>>' is called Extraction operator*/
    cout<<"The value of num 2 is:\n"; /*'<<' is called Insertion Operator*/
    cin>>num2; /*'>>' is called Extraction operator*/
    cout<<"The sum is "<<num1+num2;
    return 0;
}
